package com.leetcode.test1.chapter00002;

class ListNode {

  ListNode next;

  private int num;

  public ListNode(int num) {
    this.num = num;
  }

  public ListNode getNext() {
    return next;
  }

  public void setNext(ListNode next) {
    this.next = next;
  }

  public int getNum() {
    return num;
  }

  public void setNum(int num) {
    this.num = num;
  }
}